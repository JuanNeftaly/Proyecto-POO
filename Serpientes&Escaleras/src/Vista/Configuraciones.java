    private void btnSveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSveActionPerformed
        //Verifica el modo de juego seleccionado
        switch (modoJuego) {
            //En caso de ser un jugador
            case 1: {
                //Manda a llamar la ventana de un jugador enviandole todos los datos necesarios y cierra esta (Configuraciones)
                UnJugador unJugador = new UnJugador(ImagenSeleccionada(), ConfigCasillas(),
                        String.valueOf(cmbP1.getSelectedItem()), selectedColorP1, primera);
                unJugador.setVisible(true);

                this.dispose();

                break;
            }

            //En caso de ser dos jugadores
            case 2:
                //Verifica si los nombres de usuario son iguales
                if (cmbP1.getSelectedItem().equals(cmbP2.getSelectedItem())) {

                    //En caso de ser iguales muestra una advertencia y no permite el guardado
                    JOptionPane.showMessageDialog(null, "No puede elegir el mismo nickname dos veces",
                            "Error", JOptionPane.ERROR_MESSAGE);
                } else if (selectedColorP1.getRGB() == selectedColorP2.getRGB()) {
                    JOptionPane.showMessageDialog(null, "No puede elegir el mismo color dos veces",
                            "Error", JOptionPane.ERROR_MESSAGE);
                } else {
                    DosJugadores dosJugadores = new DosJugadores(ImagenSeleccionada(), ConfigCasillas(),
                            ConfigCasillas(), String.valueOf(cmbP1.getSelectedItem()),
                            String.valueOf(cmbP2.getSelectedItem()), selectedColorP1, selectedColorP2, primera);
                    dosJugadores.setVisible(true);

                    this.dispose();
                }

                break;
        }
    }//GEN-LAST:event_btnSveActionPerformed

