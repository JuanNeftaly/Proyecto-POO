    //Permite que la imagen sea adaptada al tamaño buscado
    public void EscalaImagen(String url) {
        ImageIcon imgIcon = new ImageIcon(getClass().getResource(url));
        Image img = imgIcon.getImage();
        Image imgScale = img.getScaledInstance(tableroLabel.getWidth(), tableroLabel.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon scaledIcon = new ImageIcon(imgScale);

        //Coloca la imagen en el label correspondiente
        tableroLabel.setIcon(scaledIcon);
    }

    //Devuelve la imagen seleccionada
    public String ImagenSeleccionada() {
        String url = "";

        //En caso de que el jugador haya seleccionado el primer tablero devuelve la direccion respectiva de la imagen
        if (primera) {
            return "/imagenes/Tablero1.jpg";

            //En caso de haber seleccionado la imagen del segundo tablero
        } else {
            return "/imagenes/Tablero2.jpg";
        }
    }

    //Cambia el valor de primera en base a las flechas presionadas para cambiar de tablero
    public void CambioBool() {
        if (primera) {
            primera = false;
        } else {
            primera = true;
        }
    }

    //Permite la configuración de las casillas en base al tablero seleccionado
    private Casillas ConfigCasillas() {
        if (primera) {
            return new Casillas(75, 355, 140, 77, 30, 6, primera);

        } else {
            return new Casillas(71, 363, 117, 55, 49, 7, primera);
        }
    }

    private Color GetColor(JSlider r, JSlider g, JSlider b, JPanel panel) {
        Color newColor = new Color(r.getValue(), g.getValue(), b.getValue());

        panel.setBackground(newColor);

        return newColor;
    }

